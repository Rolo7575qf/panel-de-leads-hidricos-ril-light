export const getInitialLeads = (): Lead[] => {
  return rawLeadsData.map((raw) => {
    // Generate some dynamic mock metadata for CRM operation
    const scoreNumeric = parseInt(raw.score.match(/\d/)?.[0] || '3');
    const assignedAgentId = INITIAL_AGENTS[raw.n % INITIAL_AGENTS.length].id;
    const agent = INITIAL_AGENTS.find((a) => a.id === assignedAgentId)!;

    // Build some historical interaction notes based on state
    const interactions = [];
    const dateStr = '2026-05-' + String((raw.n % 25) + 1).padStart(2, '0');

    if (raw.estado !== 'POR_PROSPECTAR') {
      interactions.push({
        id: `sys-init-${raw.n}`,
        type: 'system' as const,
        summary: 'Asignación de Lead',
        details: `Lead asignado automáticamente al agente ${agent.name} debido al alto estrés hídrico registrado (${raw.estresHidrico}).`,
        date: `${dateStr} 09:30`,
        agentName: 'Sistema'
      });
    }

    if (raw.estado === 'CONTACTADO' || raw.estado === 'EN_NEGOCIACION' || raw.estado === 'PROPUESTA_ENVIADA' || raw.estado === 'CERRADO_GANADO') {
      interactions.push({
        id: `int-call-${raw.n}`,
        type: 'call' as const,
        summary: 'Llamada preliminar con Dir. Medio Ambiente',
        details: `Se contactó al departamento de Medio Ambiente para agendar reunión virtual. Mostraron interés en la tecnología de membranas (reúso) por la oportunidad de: ${raw.oportunidad}.`,
        date: `${dateStr} 15:10`,
        agentName: agent.name
      });
    }

    if (raw.estado === 'EN_NEGOCIACION' || raw.estado === 'PROPUESTA_ENVIADA' || raw.estado === 'CERRADO_GANADO') {
      interactions.push({
        id: `int-meet-${raw.n}`,
        type: 'meeting' as const,
        summary: 'Reunión Técnica de Diagnóstico',
        details: `Presentación corporativa de Consorcio Ril Ligth S.A. Evaluamos el dolo hídrico: ${raw.dolorHidrico}. El municipio solicita una propuesta estimativa de m³ diarios a recircular.`,
        date: `${dateStr} 11:00`,
        agentName: agent.name
      });
    }

    if (raw.estado === 'PROPUESTA_ENVIADA' || raw.estado === 'CERRADO_GANADO') {
      interactions.push({
        id: `int-proposal-${raw.n}`,
        type: 'email' as const,
        summary: 'Propuesta Económica & Proyecto Técnico Enviado',
        details: `Envío oficial de propuesta formal y anteproyecto bajo modelo B.O.T (Build-Operate-Transfer). Se propone recircular el 95% del caudal del equipamiento deportivo/verde comunal.`,
        date: `${dateStr} 17:45`,
        agentName: agent.name
      });
    }

    if (raw.estado === 'CERRADO_GANADO') {
      interactions.push({
        id: `int-won-${raw.n}`,
        type: 'status_change' as const,
        summary: 'Firma de Contrato / Convenio de Colaboración',
        details: 'El alcalde de la comuna aprobó oficialmente adjudicar el proyecto piloto de reúso de agua para servicios municipales. Compromiso de instalación rápido.',
        date: `${dateStr} 12:00`,
        agentName: agent.name
      });
    }

    // Assign realistic contract and proposed values based on score
    const m3Proposed = scoreNumeric * (20 + (raw.n % 30)); 
    const contractValue = m3Proposed * (350 + (raw.n % 150)); // contract price in USD equivalent (monthly fee or BOT projection)

    return {
      id: `lead-${raw.n}`,
      n: raw.n,
      grupo: raw.n <= 52 ? 'RilLight_Leads_Grupo1_Actores' : 'Grupo 2',
      region: raw.region,
      comuna: raw.comuna,
      estresHidrico: raw.estresHidrico,
      alcalde: raw.alcalde,
      emailAlcaldia: raw.emailAlcaldia,
      dirMedioAmbiente: raw.dirMedioAmbiente,
      emailMA: raw.emailMA,
      telMA: raw.telMA,
      secplaNombre: raw.secplaNombre,
      secplaEmail: raw.secplaEmail,
      secplaFono: raw.secplaFono,
      gobernadorRegional: raw.gobernadorRegional,
      emailGore: raw.emailGore,
      fonoGore: raw.fonoGore,
      concejalClave: raw.concejalClave,
      emailConcejal: raw.emailConcejal,
      aprClave: raw.aprClave,
      contactoApr: raw.contactoApr,
      dolorHidrico: raw.dolorHidrico,
      oportunidad: raw.oportunidad,
      score: raw.score,
      scoreNumeric,
      estado: raw.estado as LeadState,
      assignedAgentId,
      lastContactDate: interactions.length > 0 ? interactions[interactions.length - 1].date : undefined,
      nextFollowUpDate: raw.estado === 'PROPUESTA_ENVIADA' ? '2026-06-10' : raw.estado === 'CONTACTADO' ? '24 HORAS' : undefined,
      m3Proposed,
      contractValue,
      interactions
    };
  });
};
